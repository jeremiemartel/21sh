#include "libft.h"

size_t			ft_longlen(long nb)
{
	size_t		len;

	len = 1;
	if (nb < 0)
	{
		nb = -1 * nb;
		len++;
	}
	while (nb / 10)
	{
		len++;
		nb /= 10;
	}
	return (len);
}
