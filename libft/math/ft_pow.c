#include "libft.h"

long		ft_pow(long x, long y)
{
	int		i;
	long	nb;

	nb = x;
	i = 1;
	if (y == 0)
		return (1);
	while (i < y)
	{
		nb *= x;
		i++;
	}
	return (nb);
}
