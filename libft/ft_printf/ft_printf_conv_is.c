#include "ft_printf.h"

int			conv_isconv(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'o')
		return (1);
	if (c == 'u' || c == 'x' || c == 'X' || c == 'f' || c == '%' || c == 'b')
		return (1);
	return (0);
}
