#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -1 * n;
	}
	if (n < 10)
	{
		ft_putchar('0' + n);
		return ;
	}
	ft_putnbr(n / 10);
	ft_putchar('0' + n % 10);
	return ;
}
